#include <bits/stdc++.h>

using namespace std;
const int maxn = 2e5 + 5;
long long a[maxn];
void solve() {
    int n;
    long long k;
    cin >> n >> k;
    for(int i = 1; i <= n; i ++) cin >> a[i];
}
int main() {
    ios :: sync_with_stdio(0);
    cin.tie(0), cout.tie(0);
    int T;
    cin >> T;
    while(T --) solve();
    return 0;
}